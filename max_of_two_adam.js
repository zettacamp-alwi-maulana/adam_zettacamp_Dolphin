function max_of_two(a, b) {
    if (a > b) {
      return a;
    } else {
      return b;
    }
  }
  
  
  console.log(max_of_two(5, 10)); 
  console.log(max_of_two(66, 45)); 
  console.log(max_of_two(0.9, 0.99)); 
  console.log(max_of_two(10, 5)); 
  console.log(max_of_two(45, 66));
  